def operations_on_numbers():
    number1 = int(input("Enter integer:"))
    print(f"\nYou entered: {number1}")
    print(f"{number1} squared is {number1**2}")
    print(f"And {number1} cubed is {number1**3} !!")

    number2 = int(input("Enter another integer:"))
    print(f"\n{number1} + {number2} is {number1 + number2}")
    print(f"{number1} * {number2} is {number1 * number2}")

operations_on_numbers()